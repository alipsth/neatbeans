    private void TampilData() {
         DefaultTableModel model=new DefaultTableModel();
         model.addColumn("idkaryawan");
         model.addColumn("namakaryawan");
         model.addColumn("jeniskelamin");
         model.addColumn("jabatan");
         model.addColumn("nohp");
         model.addColumn("alamat");
         tb.setModel(model);
         //menampilkan data database kedalam tabel
         try{
            java.sql.Statement stat=Koneksi2.GetConnection().createStatement();
            ResultSet data= stat.executeQuery
            ("SELECT * FROM biodatakaryawan");
            while(data.next()){
               model.addRow(new Object[]{
               data.getString("IdKaryawan"),
               data.getString("NamaKaryawan"),
               data.getString("JenisKelamin"),
               data.getString("Jabatan"),
               data.getString("NoHp"),
               data.getString("Alamat")
               });
               tb.setModel(model);
            }
         }
         catch(Exception e){
            System.err.println("terjadi kesalahan" +e );
        }
    }
