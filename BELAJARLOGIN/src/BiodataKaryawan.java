    private void tbMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbMouseClicked
        // TODO add your handling code here:
         int baris = tb.getSelectedRow();
        if (baris != -1) {
            nim.setText(tb.getValueAt(baris, 0).toString());
            nama.setText(tb.getValueAt(baris, 1).toString());
            if ("Laki-Laki".equals (tb.getValueAt(baris, 2).toString())){
                lakilaki.setSelected(true);
            }
            else {
                perempuan.setSelected(true);
            }
            jabatan.setText(tb.getValueAt(baris, 3).toString());
            nohp.setText(tb.getValueAt(baris, 4).toString());
            alamat.setText(tb.getValueAt(baris, 5).toString());
        }
    }//GEN-LAST:event_tbMouseClicked

    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
                String Nim = nim.getText();
        String Nama = nama.getText();
        String jeniskelamin = null;
        if(lakilaki.isSelected()){
            jeniskelamin="laki-laki";
        }
        else if(perempuan.isSelected()){
            jeniskelamin="perempuan";
        }
        String jabatan1=jabatan.getText();
        String nama1=nama.getText();
        String nim1=nim.getText();
        String nohp1=nohp.getText();
        String alamat1=alamat.getText();
        if(nim.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"mohon masukan data untuk di input",
                "PERHATIAN",JOptionPane.WARNING_MESSAGE);
        }

        else{
            try{
                java.sql.Statement stat =Koneksi2.GetConnection().createStatement();
                ResultSet data= stat.executeQuery("SELECT*FROM biodatakaryawan "
                    + "WHERE IdKaryawan ='"+nim.getText()+"'");
                if(data.next()){
                    JOptionPane.showMessageDialog(null, "nim sudah ada","PERHATIAN",
                        JOptionPane.WARNING_MESSAGE);
                    nim.requestFocus();
                }
                else{
                    String sql="INSERT INTO biodatakaryawan VALUES ('"+nim1+"'"
                    +",'"+nama1+"'"
                    +",'"+jeniskelamin+"'"
                    +",'"+jabatan1+"'"
                    +",'"+nohp1+"'"
                    +",'"+alamat1+"')";        
                    stat.executeUpdate(sql);
                    nim.setText("");
                    nama.setText("");
                    jabatan.setText("");
                    nohp.setText("");
                    alamat.setText("");
                    nim.requestFocus();
                    JOptionPane.showMessageDialog(null, "data berhasil disimpan","SUKSES",
                        JOptionPane.INFORMATION_MESSAGE);
                    TampilData();
                }
                stat.close();
            }catch (Exception exc){
                System.out.println("Terjadi Kesalahan :" + exc);
            }
        }
    }//GEN-LAST:event_simpanActionPerformed

