    private void tbMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbMouseClicked
        // TODO add your handling code here:
        int baris =tb.getSelectedRow();
        if (baris != -1) {
            NIM.setText(tb.getValueAt(baris, 0).toString());
            NM.setText(tb.getValueAt(baris, 1).toString());
            if("laki-laki".equals(tb.getValueAt(baris, 2).toString())) {
                laki.setSelected(true);
            }
            else {
                pr.setSelected(true);
            }
        }
        JURUS.setSelectedItem(tb.getValueAt(baris, 3).toString());
        SMTR.setText(tb.getValueAt(baris, 4).toString());
    }//GEN-LAST:event_tbMouseClicked

