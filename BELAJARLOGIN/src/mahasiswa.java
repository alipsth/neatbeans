    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
          String nim=NIM.getText();
          String nama=NM.getText();
          String semester=SMTR.getText();
        String jengkel= " ",JURUSAN;
        if (laki.isSelected()){
            jengkel="Laki-Laki";
        }
        else {
            jengkel=pr.getText();
        }
        JURUSAN=JURUS.getSelectedItem().toString();
          
        String satu[]=
        {NIM.getText(),NM.getText(),jengkel,JURUSAN,SMTR.getText()};
        FORM_MHS.addRow(satu);
        if(NIM.getText().trim().equals(" ")){
                    JOptionPane.showMessageDialog(null,"Masukan Data Untuk Di Input",
                    "PERHATIAN",JOptionPane.WARNING_MESSAGE);}
        else{
                try{
            java.sql.Statement stat=koneksi.GetConnection().createStatement();
            ResultSet data=stat.executeQuery
            ("SELECT * FROM loginmahasiswa WHERE NIM='"+NIM.getText() + "'");
            if(data.next()) {
                JOptionPane.showMessageDialog(null, "NIM SUDAH ADA","PERHATIAN",JOptionPane.WARNING_MESSAGE);
                NIM.requestFocus();}
            
           else{
                        String sql="INSERT INTO loginmahasiswa VALUES('"+nim+"'"
                  +",'" +nama+"'"
                  +",'" +jengkel+"'"
                  +",'" +JURUSAN+"'"
                  +",'" +semester+"')";
                        stat.executeUpdate(sql);
                        NIM.setText(" ");
                        NM.setText(" ");
                        JURUS.setSelectedIndex(-0);
                        SMTR.setText(" ");
                        NIM.requestFocus();
                        JOptionPane.showMessageDialog(null, "Data berhasil di simpan","Sukses",
                        JOptionPane.INFORMATION_MESSAGE);
                        TampilData();
                }
                
           stat.close();
                }    
           catch (Exception ext) {
        System.out.println("Terjadi Kesalahan : " + ext);
          }
       }         
    }//GEN-LAST:event_simpanActionPerformed

