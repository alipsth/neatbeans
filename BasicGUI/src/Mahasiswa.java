    private void tmbhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tmbhActionPerformed
        // TODO add your handling code here:
        String jengkel= " ",agama;
        if (laki.isSelected()){
            jengkel="Laki-Laki";
        }
        else {
            jengkel=pr.getText();
        }
        agama=pl.getSelectedItem().toString();
        String satu[]=
        {ns.getText(),nama.getText(),am.getText(),jengkel,agama};
        data_mhs.addRow(satu);
    }//GEN-LAST:event_tmbhActionPerformed

