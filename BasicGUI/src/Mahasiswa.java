    private void hpsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hpsActionPerformed
        // TODO add your handling code here:
        DefaultTableModel data_mhs = (DefaultTableModel) tb.getModel();
        int row=tb.getSelectedRow();
        if(row>=0){
            int ok=JOptionPane.showConfirmDialog(null,"Jangan Keluar Dlu Ath Lahhh..",
                    "Kompirmasi",JOptionPane.YES_NO_OPTION);
            if(ok==0){
                data_mhs.removeRow(row);
            }
        }
    }//GEN-LAST:event_hpsActionPerformed

